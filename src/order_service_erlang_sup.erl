%%%-------------------------------------------------------------------
%%% @doc 主监督树模块
%%% @end
%%%-------------------------------------------------------------------
-module(order_service_erlang_sup).
-behaviour(supervisor).

-export([start_link/0]).
-export([init/1]).

-define(SERVER, ?MODULE).

%%--------------------------------------------------------------------
%% @doc 启动监督树
%% @end
%%--------------------------------------------------------------------
start_link() ->
    supervisor:start_link({local, ?SERVER}, ?MODULE, []).

%%--------------------------------------------------------------------
%% @doc 初始化监督树
%% @end
%%--------------------------------------------------------------------
init([]) ->
    % 定义监督策略
    SupFlags = #{
        strategy => one_for_one,  % 一个子进程崩溃时，只重启该进程
        intensity => 10,          % 10秒内最多重启10次
        period => 10
    },
    
    % 定义子进程规范
    % 目前没有需要监督的子进程，但可以在这里添加
    ChildSpecs = [],
    
    {ok, {SupFlags, ChildSpecs}}.